		<h2>Der Keller</h2>

		<p>
			Der Schlosskeller ist ein autonomes Gewerbe des AStA der TU Darmstadt und existiert bereits
			seit 1966 als Veranstaltungsraum und Club im Kellergewölbe des Schlosses mitten in Darmstadt.
			Die Entwicklung, Planung und Durchführung von Veranstaltungen wird vor allem von Studierenden
			realisiert.
		</p>

		<p>
			Die Preispolitik der angebotenen Getränke und Speisen richtet sich generell an studentisches
			Publikum. Der Schlosskeller ist kein gewinnorientiertes Unternehmen, schafft aber dennoch
			Arbeitsplätze. Er bietet eine Aufwertung des studentischen Lebens, von Studierenden für
			Studierende, aber natürlich auch für jede_n Andere_n. Durch ehrenamtliches Engagement können
			alle Mitarbeiter_Innen Projekte im Schlosskeller mitgestalten. Die Arbeit im Schlosskeller
			basiert auf Mitbestimmung des Teams und demokratischen Strukturen.
		</p>
	</article>

	<p>
		Das Programm des Schlosskellers ist auf Vielfalt ausgelegt und bietet etwas für jeden Geschmack.
		Die Vielfalt zeigt sich durch verschiedene Veranstaltungskonzepte wie beispielsweise Kleinkunst
		in Form der Kulturhäppchen-Reihe, Vorträge, Kinoabende oder Konzert- und Clubevents. Viele
		Veranstaltungen werden regelmäßig angeboten, wobei wir immer darauf bedacht sind unsere Konzepte
		zu optimieren und neue Ideen einfließen zu lassen.
	</p>

